//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
#ifndef CTIMER_H
#define CTIMER_H
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#include <boost/timer.hpp>
#include <boost/thread.hpp>
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#define	LPTHREAD				LPTHREAD_START_ROUTINE
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

class CTimer
{
private:
	ULONG	m_Tick;
	bool	m_bWorker;
	UINT	m_Ticket;
	// ----
	UINT	m_nMilliseconds;
	UINT	m_nSeconds;
	UINT	m_nMinutes;
	UINT	m_nHours;
	UINT	m_nDays;
public:
	CTimer(void);
	~CTimer(void);
	// ----
	void			Init();
	// ----
	static DWORD	TimerWorker(LPVOID lpParam);
	// ----
	void			OnSecond();
	void			OnMinute();
	void			OnHour();
	void			OnDay(){};
	// ----
	inline UINT	getMilliseconds(){return (m_Tick - GetTickCount());};
	inline UINT	getSeconds(){return m_nSeconds;};
	inline UINT	getMinutes(){return m_nMinutes;};
	inline UINT	getHours(){return m_nHours;};
	inline UINT	getDays(){return m_nDays;};
};
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
#endif
